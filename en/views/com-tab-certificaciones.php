			<div id="oms" class="content-cert active">
				<h2 class="animated fadeInLeft">WHO</h2>
				<p class="animated fadeInLeft">It is the agency of the United Nations (UN) specialized in creating, implementing and evaluating policies for prevention, health promotion and intervention worldwide. It is responsability of WHO to ensure access to pharmaceutical products of good quality, safety and efficiency through pre-program evaluation of medicines. WHO pre evaluates drugs laboratories request. Being a manufacturer backed by such international organization gives consumers greater assurance about the quality and effectiveness of the products that the organization provides.</p>
			</div>
			
			<div id="fda" class="content-cert">
				<h2 class="animated fadeInLeft">FDA</h2>
				<p class="animated fadeInLeft">The FDA (Food and Drug Administration: Food and Drug Agency or the Food and Drugs) is the government agency of the United States responsible for the regulation of food, drugs (human and veterinary), cosmetics, medical (human apparatus and animal), biologics and blood derived products. The main goal of the FDA is to regulate the multitude of medicinal products in a manner that ensures consumer safety and effectiveness of marketed drugs. This is one of the toughest quality standards to obtain due to their high standards and requirements.</p>
			</div>
			
			<div id="bpm" class="content-cert">
				<h2 class="animated fadeInLeft">GMP</h2>
				<p class="animated fadeInLeft">The GMP (Good Manufacturing Practice English, GMP) are applicable to manufacturing operations drugs, cosmetics, medical products, food and drugs, in their final forms of retail including large-scale processes in hospitals and preparing supplies for the use of clinical trials for drugs. Some requirements of GMP standards are: There must be properly trained and qualified employees, adequate facilities, shipping and warehouse, among others.</p>
			</div>

			<div id="iso" class="content-cert">
				<h2 class="animated fadeInLeft">ISO9001</h2>
				<p class="animated fadeInLeft">The ISO 9001 is an international standard that applies to quality management processes and focuses on all elements of quality that a business must have in order to achive an effective system that allows managing and improving the quality of its products or services.</p>
