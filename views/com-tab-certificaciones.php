			<h1 class="animated fadeInUp">Certificaciones</h1>

			<div id="oms" class="content-cert active">
				<h2 class="animated fadeInLeft">OMS</h2>
				<p class="animated fadeInLeft">Es el organismo de la Organización de las naciones unidas (ONU) especializado en crear, ejecutar y evaluar políticas de prevención, promoción e intervención en salud a nivel mundial. Es labor de la OMS garantizar el acceso a productos farmacéuticos de buena calidad, seguridad y eficacia mediante el programa de pre-evaluación de medicamentos. La OMS pre evalúa los medicamentos de los laboratorios que lo solicitan. El ser una empresa fabricante avalada por tal organismo internacional da a los consumidores una mayor seguridad con respecto a la calidad y eficacia de los productos que dicha organización ofrece.</p>
			</div>
			
			<div id="fda" class="content-cert">
				<h2 class="animated fadeInLeft">FDA</h2>
				<p class="animated fadeInLeft">La FDA (Food and Drug Administration: Agencia de Alimentos y Medicamentos o Agencia de Drogas y Alimentos ) es la agencia del gobierno de los Estados Unidos responsable de la regulación de alimentos , medicamentos (humanos y veterinarios), cosméticos, aparatos médicos (humanos y animales), productos biológicos y derivados sanguíneos. El objetivo principal de la FDA es regular la multitud de productos medicinales de una manera tal que asegure la seguridad de los consumidores  y la efectividad de los medicamentos comercializados. Este es uno de los estándares de calidad más difíciles de obtener debido a sus rigurosas normas y requisitos.</p>
			</div>
			
			<div id="bpm" class="content-cert">
				<h2 class="animated fadeInLeft">BPM</h2>
				<p class="animated fadeInLeft">Las buenas prácticas de fabricación (en inglés Good Manufacturing Practice, GMP) son aplicables a las operaciones de fabricación de medicamentos, cosméticos, productos médicos, alimentos y drogas, en sus formas definitivas de venta al público incluyendo los procesos a gran escala en hospitales y la preparación de suministros para el uso de ensayos clínicos para el caso de medicamentos. Algunas exigencias de las normas GMP son las siguientes: Se debe contar con personal entrenado y apropiadamente calificado para procesos, instalaciones adecuadas, transporte y deposito apropiados, entre otros.</p>
			</div>

			<div id="iso" class="content-cert">
				<h2 class="animated fadeInLeft">ISO9001</h2>
				<p class="animated fadeInLeft">La ISO 9001 es una norma internacional que se aplica a los sistemas de gestión de calidad  y que se centra en todos los elementos de administración de calidad con los que una empresa debe contar para tener un sistema efectivo que le permita administrar y mejorar la calidad de sus productos o servicios.</p>
			</div>
