            <p class="animated fadeInLeft">La FDA (Food and Drug Administration: Agencia de Alimentos y Medicamentos o Agencia de Drogas y Alimentos ) es la agencia del gobierno de los Estados Unidos responsable de la regulación de alimentos , medicamentos (humanos y veterinarios), cosméticos, aparatos médicos (humanos y animales), productos biológicos y derivados sanguíneos. El objetivo principal de la FDA es regular la multitud de productos medicinales de una manera tal que asegure la seguridad de los consumidores  y la efectividad de los medicamentos comercializados. Este es uno de los estándares de calidad más difíciles de obtener debido a sus rigurosas normas y requisitos.</p>
